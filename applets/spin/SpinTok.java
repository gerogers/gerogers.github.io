import java.awt.*;

public class SpinTok
{
	String	strName;
	String	strParams[];
	String	strAttrNames[];
	String	strAttrVals[];
	public SpinTok(String name, String params[], String attrNames[],
				   String attrVals[])
	{
		this.strName = name;
		this.strParams = params;
		this.strAttrNames = attrNames;
		this.strAttrVals = attrVals;
	}
}
